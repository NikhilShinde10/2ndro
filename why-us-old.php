              <p class="text-block__desc mb-20">Patients or their loved ones commonly look for 2nd Doctor’s Opinion (2nDrO) when 
the patient has received a life-changing diagnosis. For example, they received the 
diagnosis of Multiple Sclerosis, Motor Neuron Disease, Parkinson’s Disease, or 
Cancer. The reason could be to confirm the diagnosis, to know the most recent 
treatment options or the BEST treatment options, to understand the course of the 
disease so that the family can plan coping strategies. If the disease has a genetic 
basis, the family may want to know about prenatal diagnosis or diagnosis at an 
earlier stage so that the child can avoid further complex treatments. The classic 
example is the early diagnosis of Wilson’s disease in the siblings before they suffer 
liver damage thus avoiding the need for liver transplant. Similarly, one could detect 
Porphyria in siblings and prevent future catastrophes.</p>
              <p class="text-block__desc mb-20">Sometimes the first consulting doctor will encourage a patient to seek 2nd Doctor’s Opinion (2nDrO) because that doctor does not have the required skills to manage that patient. For example, a plastic surgeon from Guy’s Hospital referred a patient to his colleague at Rickmansworth Hospital which was the only department in the country that could potentially offer the level of complex reconstruction surgery necessary for this patient. Another example is a neurosurgeon not familiar with Deep Brain Stimulation (DBS) will refer the patient requiring DBS to a neurosurgeon with that expertise.</p>
              <p class="text-block__desc mb-20">The patient may also seek for 2nDrO because the treatment given by the first doctor 
is not giving them relief. The classic examples are Headaches, Neuralgia (e.g. 
Trigeminal Neuralgia), Sciatica, Epilepsy, Peptic ulcer, Constipation, and other 
chronic conditions.</p>
              <p class="text-block__desc mb-20">Nowadays, patients routinely seek 2nDrO when they are offered high-risk and costly 
operations such as a hip replacement or knee replacement surgery. One of the 
reasons is that they may perceive a conflict of interest during their first consultation 
or, that they cannot decide on the surgery based on the information given by the first 
consultant. We had a patient who was advised of a Total Knee Replacement. This 
patient took a 2nDrO in a Centre of Clinical Excellence. He was offered a lifestyle 
change regime. Ten years since the patient is glad that they did not go for the total 
knee replacement</p>
              <p class="text-block__desc mb-20">Patients may feel that their first doctor did not listen to them properly or did not 
satisfactorily answer their queries OR the patient realizes later that they now need to 
seek more answers. In such situations, one must obtain a 2nDrO.</p>
